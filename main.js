function drawVisualization() {
  if (svgHeight < 800) {
    svgHeight = 800
  }
  let svg = d3
    .select("#chart-area")
    .append("svg")
    .attr("width", vw)
    .attr("height", svgHeight)

  let deathData = currentDataSet.map(x => x.deaths / actuator)
  let casesData = currentDataSet.map(x => x.cases / actuator)
  let recoveredData = currentDataSet.map(x => x.recovered / actuator)
  let nameData = currentDataSet.map(x => x.country)

  let deathCircles = svg.selectAll("circle").data(deathData)
  let casesCircles = svg.selectAll("circle").data(casesData)
  let recoveredCircles = svg.selectAll("circle").data(recoveredData)
  let nameLabels = svg.selectAll("text").data(nameData)

  let cum = 0
  let casesCumData = []
  let radData = []
  casesCircles
    .enter()
    .append("circle")
    .attr("cx", 0)
    .attr("cy", (d, i) => {
      cum += d + d
      casesCumData.push(cum - d)
      radData.push(d)
      return (cum - d).toFixed(3)
    })
    .attr("r", function(d) {
      return d.toFixed(3)
    })
    .attr("fill", "steelblue")

  recoveredCircles
    .enter()
    .append("circle")
    .attr("cx", 0)
    .attr("cy", (d, i) => {
      return casesCumData[i].toFixed(3)
    })
    .attr("r", function(d) {
      return d.toFixed(3)
    })
    .attr("fill", "rgb(255, 196, 0)")

  deathCircles
    .enter()
    .append("circle")
    .attr("cx", 0)
    .attr("cy", (d, i) => {
      return casesCumData[i].toFixed(3)
    })
    .attr("r", function(d) {
      return d.toFixed(3)
    })
    .attr("fill", "tomato")

  // Country labels
  nameLabels
    .enter()
    // .append("a")
    // .attr("xlink:href", d => {
    //   return `/countries/${d}`
    // })
    .append("text")
    .attr("x", (d, i) => {
      return (radData[i] + 2).toFixed(1)
    })
    .attr("y", (d, i) => {
      if (i < 8) {
        return (casesCumData[i] + radData[i] / (16 - i - 6)).toFixed(1)
      } else {
        return (casesCumData[i] + radData[i] / 2).toFixed(1)
      }
    })
    .text(function(d, i) {
      return d
    })
    .attr("font-family", countryNameFont)
    .attr("font-size", (d, i) => {
      if (i === 0) {
        return (radData[i] / 2.5).toFixed(1)
      } else if (i < 5) {
        return (radData[i] / 1.5).toFixed(1)
      } else if (i < 7) {
        return (radData[i] / 1.2).toFixed(1)
      } else {
        return radData[i].toFixed(1)
      }
    })
    .attr("dominant-baseline", "ideographic")
    .attr("dy", (d, i) => {
      if (isSafari) {
        if (i === 0) {
          return (radData[i] * -0.15).toFixed(1)
        } else if (i < 7) {
          return (radData[i] * -0.35).toFixed(1)
        } else {
          return (radData[i] * -0.6).toFixed(1)
        }
      }
    })
    .attr("id", function(d, i) {
      return "country_" + i
    })
    .attr("class", function(d, i) {
      return "countryLink"
    })

  // Cases, recoveries and deaths
  nameLabels
    .enter()
    .append("text")
    .text(function(d, i) {
      let displayCases = currentDataSet[i].cases
        .toString()
        .replace(/\B(?=(\d{3})+(?!\d))/g, ",")
      let displayRecovered = currentDataSet[i].recovered
        .toString()
        .replace(/\B(?=(\d{3})+(?!\d))/g, ",")
      let displayDeaths = currentDataSet[i].deaths
        .toString()
        .replace(/\B(?=(\d{3})+(?!\d))/g, ",")
      return (
        displayCases +
        " cases" +
        "\xa0\xa0" +
        displayRecovered +
        " recoveries" +
        "\xa0\xa0" +
        displayDeaths +
        " deaths"
      )
    })
    .attr("font-family", countryDataFont)
    .attr("font-size", (d, i) => {
      if (i == 0) {
        return (radData[i] / 6).toFixed(1)
      } else if (i < 3) {
        return (radData[i] / 3.5).toFixed(1)
      } else if (i < 4) {
        return (radData[i] / 2.5).toFixed(1)
      } else if (i < 5) {
        return (radData[i] / 2.5).toFixed(1)
      } else if (i < 7) {
        return (radData[i] / 2).toFixed(1)
      } else if (i < 9) {
        return (radData[i] / 1.4).toFixed(1)
      } else {
        return (radData[i] / 1.2).toFixed(1)
      }
    })
    .attr("x", (d, i) => {
      return radData[i] + 2
    })
    .attr("y", (d, i) => {
      if (i === 0) {
        return casesCumData[i]
      } else if (i < 8) {
        return casesCumData[i] + 1
      } else {
        return casesCumData[i] + radData[i] / 4
      }
    })
    .attr("dominant-baseline", "hanging")
    .attr("dy", (d, i) => {
      if (isSafari && i === 0) {
        return radData[i] * 0.1
      } else if (i < 8) {
        return radData[i] * 0.1
      } else {
        return radData[i] * 0.05
      }
    })
}
